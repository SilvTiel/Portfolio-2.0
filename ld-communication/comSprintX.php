                <h1>Usabilty Test</h1>
                <p>Nadat ik versie 1 had gemaakt van mijn app ben ik gaan kijken of de gebruiker de app begreep. Hiervoor heb ik een aantal opdrachten gemaakt die de doelgroep moest uitvoeren. Dit heb ik gedaan om de gebruikerservaring te verbeteren. De opdrachten bestonden uit 7 handige vragen zoals:"Zorg ervoor dat de punten telling uit komt te staan." Nadat ze klaar waren met het uitvoeren van de opdrachten heb ik ze nog een paar vragen gesteld die te maken hadden met de opdrachten. Deze vragen gingen allemaal over de app met als voorbeeld:"Moet de leider zelf ook de code invullen?"
